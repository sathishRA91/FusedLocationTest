package com.ufours.fusedlocationtest.model;

public class ItemModel {

    private int key_favourite_id;

    private String favourite_id;

    private String favourite_title_name;

    private String favourite_description;

    private String favourite_author;

    private String favourite_date_time_ago;


    public int getKey_favourite_id() {
        return key_favourite_id;
    }

    public void setKey_favourite_id(int key_favourite_id) {
        this.key_favourite_id = key_favourite_id;
    }

    public String getFavourite_id() {
        return favourite_id;
    }

    public void setFavourite_id(String favourite_id) {
        this.favourite_id = favourite_id;
    }

    public String getFavourite_title_name() {
        return favourite_title_name;
    }

    public void setFavourite_title_name(String favourite_title_name) {
        this.favourite_title_name = favourite_title_name;
    }

    public String getFavourite_description() {
        return favourite_description;
    }

    public void setFavourite_description(String favourite_description) {
        this.favourite_description = favourite_description;
    }

    public String getFavourite_author() {
        return favourite_author;
    }

    public void setFavourite_author(String favourite_author) {
        this.favourite_author = favourite_author;
    }

    public String getFavourite_date_time_ago() {
        return favourite_date_time_ago;
    }

    public void setFavourite_date_time_ago(String favourite_date_time_ago) {
        this.favourite_date_time_ago = favourite_date_time_ago;
    }
}
